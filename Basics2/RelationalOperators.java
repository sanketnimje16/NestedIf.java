package Basics2;

class RelationalOperators {
    public static void main(String[] args) {

        int firstNumber = 12;
        int secondNumber = 16;
        System.out.println(firstNumber == secondNumber);
        System.out.println(firstNumber != secondNumber);
        System.out.println(firstNumber > secondNumber);
        System.out.println(firstNumber < secondNumber);
        System.out.println(firstNumber >= secondNumber);
        System.out.println(firstNumber <= secondNumber);
        
    }

    // Bitwise Operator have been skip. 
}
